class IfElse {
	public static void main (String[] args) {
		// int x = 4;
		int x = 3;
		if (x == 3) {
			System.out.println("This is will print only X is equal 3");
		}
		else {
			System.out.println("This is will print only X is not equal 3");
		}
		System.out.println("This is will print no matter what");
	}
}
